class ArtworkShare < ApplicationRecord

  validate :validate_ownership

  belongs_to :artwork,
    primary_key: :id,
    foreign_key: :artwork_id,
    class_name: :Artwork

  belongs_to :viewer,
    primary_key: :id,
    foreign_key: :viewer_id,
    class_name: :User

  def validate_ownership
    if Artwork.find(artwork_id).artist_id == viewer_id
      errors[:artist] << "cannot share own work with yourself"
    end
  end
end
